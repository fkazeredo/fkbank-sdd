-- Persistent event publication registry: the outbox base.
--
-- Owned by Flyway rather than auto-created at startup, so the shape of a table that will hold
-- unprocessed business facts is reviewed like any other schema change.
-- Mirrors the framework's own PostgreSQL schema (v2). Kept identical on purpose: the framework
-- reads and writes these columns, so a well-meaning improvement here would break it at startup.
CREATE TABLE event_publication (
    id                     UUID        NOT NULL PRIMARY KEY,
    listener_id            TEXT        NOT NULL,
    event_type             TEXT        NOT NULL,
    serialized_event       TEXT        NOT NULL,
    publication_date       TIMESTAMPTZ NOT NULL,
    completion_date        TIMESTAMPTZ,
    status                 TEXT,
    completion_attempts    INT,
    last_resubmission_date TIMESTAMPTZ
);

CREATE INDEX event_publication_serialized_event_hash_idx
    ON event_publication USING hash (serialized_event);
CREATE INDEX event_publication_by_completion_date_idx
    ON event_publication (completion_date);
