-- The accounting core: the chart of accounts, the postings that move money between them, and
-- the running balance each account holds.
--
-- The schema is written so that the states the ledger must never reach are impossible here, not
-- merely discouraged in Java. Application code can be bypassed by a migration, a support script
-- or a future mistake; a trigger and a unique index cannot.
--
-- All timestamps are UTC.

-- An account in the chart of accounts. Reference data for the internal accounts, seeded below;
-- customer and box accounts are opened as people and boxes are created.
CREATE TABLE account (
    id          BIGINT      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code        TEXT        NOT NULL UNIQUE,
    kind        TEXT        NOT NULL,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT account_kind_known CHECK (kind IN (
        'CUSTOMER_AVAILABLE',
        'CUSTOMER_BOX',
        'INTERNAL_SETTLEMENT',
        'INTERNAL_EXPENSE',
        'INTERNAL_CREDIT'
    ))
);

COMMENT ON TABLE account IS
    'Chart of accounts. Id order is the order balance locks are acquired in.';

-- One movement of money. Append-only: see the trigger below.
CREATE TABLE posting (
    id                  UUID        PRIMARY KEY,
    debit_account_id    BIGINT      NOT NULL REFERENCES account (id),
    credit_account_id   BIGINT      NOT NULL REFERENCES account (id),
    amount              NUMERIC(19, 4) NOT NULL,
    currency            VARCHAR(3)  NOT NULL,
    occurred_at         TIMESTAMPTZ NOT NULL,
    reverses_posting_id UUID        REFERENCES posting (id),
    CONSTRAINT posting_amount_positive CHECK (amount > 0),
    CONSTRAINT posting_between_two_accounts CHECK (debit_account_id <> credit_account_id)
);

COMMENT ON TABLE posting IS
    'Append-only double-entry movements. Corrections are contra-postings, never edits.';

CREATE INDEX posting_debit_account_idx ON posting (debit_account_id);
CREATE INDEX posting_credit_account_idx ON posting (credit_account_id);

-- A posting is reversed at most once.
--
-- The ledger refuses a second reversal before it gets here, but that check reads the table and
-- then writes to it: two concurrent reversals can both read "not yet reversed". This index is
-- what makes the second one fail. Without it, the extra contra-posting would land on an account
-- allowed to go negative, producing a balance that is fabricated yet perfectly self-consistent -
-- invisible to a trial balance, because debits would still equal credits.
CREATE UNIQUE INDEX posting_reverses_at_most_once_idx
    ON posting (reverses_posting_id)
    WHERE reverses_posting_id IS NOT NULL;

-- A reversal never points at itself.
ALTER TABLE posting ADD CONSTRAINT posting_does_not_reverse_itself
    CHECK (reverses_posting_id IS NULL OR reverses_posting_id <> id);

-- A reversal is never itself reversed.
--
-- The unique index above stops one posting being reversed twice, but says nothing about a chain:
-- A reversed by B, then B reversed by C. Each link is unique, and the balances still add up,
-- while the money has moved back and forth a third time on a correction nobody authorised. A
-- CHECK cannot see another row, so this needs a trigger.
CREATE OR REPLACE FUNCTION posting_reversal_is_not_reversed() RETURNS TRIGGER AS $$
BEGIN
    IF NEW.reverses_posting_id IS NOT NULL
       AND EXISTS (SELECT 1 FROM posting
                   WHERE id = NEW.reverses_posting_id
                     AND reverses_posting_id IS NOT NULL) THEN
        RAISE EXCEPTION 'posting % is a reversal and cannot be reversed', NEW.reverses_posting_id
            USING ERRCODE = 'integrity_constraint_violation';
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER posting_no_reversal_chain
    BEFORE INSERT ON posting
    FOR EACH ROW EXECUTE FUNCTION posting_reversal_is_not_reversed();

-- Immutability, enforced where it cannot be argued with.
CREATE OR REPLACE FUNCTION posting_is_append_only() RETURNS TRIGGER AS $$
BEGIN
    -- Names the table it actually fired on. Hardcoding one table's name misdirects whoever reads
    -- the error when the same guard protects more than one.
    RAISE EXCEPTION '% is append-only: % on % is not permitted',
        TG_TABLE_NAME, TG_OP, TG_TABLE_NAME
        USING ERRCODE = 'integrity_constraint_violation';
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER posting_no_update_or_delete
    BEFORE UPDATE OR DELETE ON posting
    FOR EACH ROW EXECUTE FUNCTION posting_is_append_only();

-- TRUNCATE is not a DELETE as far as triggers are concerned: it removes every row without
-- firing a row-level trigger even once, so the guard above would watch an empty table being
-- emptied. It needs its own statement-level trigger, or one statement erases the ledger.
CREATE TRIGGER posting_no_truncate
    BEFORE TRUNCATE ON posting
    FOR EACH STATEMENT EXECUTE FUNCTION posting_is_append_only();

-- What an account currently holds.
--
-- A convenience, not the truth: the postings are the truth. Keeping it saved avoids replaying an
-- account's entire history on every read, and the trial balance exists to prove the two still
-- agree. Rows are locked FOR UPDATE, in ascending account id order, while a posting is applied.
CREATE TABLE balance (
    account_id BIGINT      PRIMARY KEY REFERENCES account (id),
    amount     NUMERIC(19, 4) NOT NULL,
    currency   VARCHAR(3)  NOT NULL
);

COMMENT ON TABLE balance IS
    'Materialized balance per account, verified against the postings by the trial balance.';

-- Balances may legitimately be updated - that is what a posting does - and a row is created when
-- an account is opened. Neither removal nor wholesale wiping is ever legitimate: a posting can
-- never be deleted, so a balance row that disappears leaves an account whose history says it holds
-- money and whose position no longer exists. Guarding TRUNCATE alone leaves DELETE as an open door
-- to the same state.
CREATE TRIGGER balance_no_truncate
    BEFORE TRUNCATE ON balance
    FOR EACH STATEMENT EXECUTE FUNCTION posting_is_append_only();

CREATE TRIGGER balance_no_delete
    BEFORE DELETE ON balance
    FOR EACH ROW EXECUTE FUNCTION posting_is_append_only();

